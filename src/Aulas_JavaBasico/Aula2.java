package Aulas_JavaBasico;

public class Aula2 {

    public static void main (String [] args) {

        int numero = 5;

        numero = numero + 2; // soma (7)
        System.out.println(numero);

        numero++; // outra forma de realizar uma soma (8)
        System.out.println(numero);

        numero = numero - 2; // subtração (6)
        System.out.println(numero);

        numero--; // outra forma de realizar uma subtração (5)
        System.out.println(numero);
    }
}