package Aulas_JavaPOO;

public class Aula16 {

    public static void main(String[] args) {

        System.out.println("Maria está contando carneirinhos...");
        System.out.println("-----------------------------------");

        for (int i = 1; i <= 20; i++ ) {
            // contador começa no 1;
            // enquanto contador for menor ou igual a 20, conte;
            // contar de 1 em 1.
            System.out.println(i + " carneirinhos");
        }
        System.out.println("-----------------------------------");
        System.out.println("Maria dormiu.");
    }
}