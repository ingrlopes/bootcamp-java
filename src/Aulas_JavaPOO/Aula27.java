package Aulas_JavaPOO;

public class Aula27 {

    public static void main (String [] args) {

        int numero = 5; // número naturalmente positivo (+)

        numero = - numero; // transformação do número positivo (5) para negativo (-5)
        System.out.println(numero);

        numero = numero * -1; // conversão para positivo novamente (5)
        System.out.println(numero);
    }
}