package Escola;

public class Aluno {

    private String nome;
    private int idade;

    public void setNome(String newNome) {
        this.nome = newNome;
    }
    public String getNome() {
        return nome;
    }

    public void setIdade(int newIdade) {
        this.idade = newIdade;
    }
    public int getIdade() {
        return idade;
    }
}